import { getWinnerImages } from '@/lib/winner-images';

export interface CheckWinner {
  id: string;
  name: string;
  location: string;
  amount: string;
  caption?: string;
  imageCount?: number;
  images?: string[];
}

export function getCheckWinnerImages(winner: CheckWinner): string[] {
  return getWinnerImages('checks', winner.id, {
    images: winner.images,
    imageCount: winner.imageCount,
  });
}

export function getCheckWinnerImage(winner: CheckWinner): string {
  return getCheckWinnerImages(winner)[0];
}

